'''
     A program to demonstrate the use of functions
'''
# in-built function call
print("Hello, World!")

# function definition
def add_two(i, j):

     # add the two numbers
     # and store the result in a variable
     result = i + j

     # return the result
     return result

# calls the function to add two numbers
answer = add_two(5, 7)

# print out the result to the console
print(answer)